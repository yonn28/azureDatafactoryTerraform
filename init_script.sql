CREATE TABLE Movies (
    Film VARCHAR(1000) NULL,
    Genre VARCHAR(1000) NULL,
    LeadStudio VARCHAR(1000) NULL,
    AudienceScorePercent VARCHAR(1000) NULL,
    Profitability VARCHAR(1000) NULL,
    RottenTomatoesPercent VARCHAR(1000) NULL,
    WorldwideGross VARCHAR(1000) NULL,
    Year VARCHAR(1000) NULL
);